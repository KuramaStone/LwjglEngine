
package me.brook.wonder.chunk;

public class Coords   {

	private int x, z;

	public Coords(int x, int z) {
		super();
		this.x = x;
		this.z = z;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getZ() {
		return z;
	}

	public void setZ(int z) {
		this.z = z;
	}

}
